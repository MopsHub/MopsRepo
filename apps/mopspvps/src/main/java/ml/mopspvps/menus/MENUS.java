package ml.mopspvps.menus;

/**
 * Enum, который содержит 4 переменных: CUSTOMGIVE (команда /customgive), KITS (для китов), POPIT (для попита) и SIMPLEDIMPLE (для симпл-димлпа)
 */
public enum MENUS {
	/**
	 * CUSTOMGIVE - инвентарь для команды /customgive
	 */
	CUSTOMGIVE,
	/**
	 * KITS - инвентарь для меню выбора китов
	 */
	KITS,
	/**
	 * POPIT - инвентарь для попита
	 */
	POPIT,
	/**
	 * SIMPLEDIMPLE - инвентарь для симпл-димпла
	 */
	SIMPLEDIMPLE
}
